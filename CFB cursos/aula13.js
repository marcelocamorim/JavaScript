if(num > 10){
    console.log('Número maior que 10')
    if(num>50){
        console.log('Número maior que 50')
    }
}else if(num >5){
    console.log('número está entre 6 e 10')
}else{
    console.log('Número igual ou menor que 5')
}

console.log('Fim do Programa')

//quando usado apenas IF se a condição especificada for 'true' ele executa a tarefa caso seja false ele ignora a tarefa e continua o código

